public class Forloop {
    public class NestedForLoop {
        public static void main(String[] args) {
            // loop of i
            for (int i = 1; i <= 3; i++) { // outer loop
                // loop of j
                for (int j = 1; j <= 3; j++) { // inner loop
                    System.out.println("value of i:" + i + " " + "value of j:" + j);

                } // end of i
            } // end of j
        }

    }

}
